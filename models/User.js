const mongoose = require('mongoose');
mongoose.set('useCreateIndex', true); // added to rid warning https://github.com/Automattic/mongoose/issues/6890

const UserSchema = mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  date: {
    type: Date,
    default: Date.now,
  },
});

module.exports = mongoose.model('user', UserSchema);
